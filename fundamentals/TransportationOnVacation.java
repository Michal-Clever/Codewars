package fundamentals;

public class TransportationOnVacation {
    public static void main(String[] args) {

        int d = 7;
        if (d < 3) {
            System.out.println(40 * d);
        } else if (d < 7) {
            System.out.println(40 * d - 20);
        } else
            System.out.println(40 * d - 50);

    }
}
